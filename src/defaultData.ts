import { VocabItem } from './types';

export const parseRawData = (text: string): VocabItem[] => {
  return text
    .split('\n')
    .filter((line) => line.trim() !== '')
    .map((line, index) => {
      const parts = line.split('|').map((p) => p.trim());
      return {
        id: `item-${index}`,
        english: parts[0] || '',
        type: parts[1] || '',
        chinese: parts[2] || '',
        engSentence: parts[3] || '',
        chnSentence: parts[4] || '',
      };
    });
};
